#AND (&)
resultado = True & True #Devuelve True
resultado1 = False & True #Devuelve False
resultado2 = True & False #Devuelve False
resultado3 = False & False #Devuelve False

#OR  (|)
resultado4 = True | True #devuelve true
resultado5 = False | True #Devuelve True
resultado6 = True | False #Devuelve True
resultado7 = False | False #Devuelve False


# NOT
resultado8 = not True #Devuelve Falso
resultado9 = not False #Devuelve True

print("AND")
print(resultado)
print(resultado1)
print(resultado2)
print(resultado3)

print("OR")
print(resultado4)
print(resultado5)
print(resultado6)
print(resultado7)

print("NOT")
print(resultado8)
print(resultado9)
